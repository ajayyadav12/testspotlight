--  DDL for View V_NOTIFICATION_LOG
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE VIEW "V_NOTIFICATION_LOG" ("SUBJECT", "PROCESS_NAME", "ID", "NOTIFICATION_DEF_ID", "SUBMISSION_ID", "SENDTIME", "TO_EMAILS", "NOTIFICATION_TEMPLATE_ID", "SCHEDULED_SUBMISSION_ID", "PROCESS_ID", "SCHEDULE_DEF_ID") AS 
  SELECT nt.subject, p.name Process_Name, nl."ID",nl."NOTIFICATION_DEF_ID",nl."SUBMISSION_ID",nl."SENDTIME",nl."TO_EMAILS",nl."NOTIFICATION_TEMPLATE_ID",nl."SCHEDULED_SUBMISSION_ID",nl."PROCESS_ID", ss.schedule_def_id
FROM T_Notification_log nl
LEFT JOIN T_notification_template nt ON nl.notification_template_id = nt.id
LEFT JOIN T_scheduled_submission ss ON nl.scheduled_submission_id = ss.id
LEFT JOIN T_process p ON nl.process_id = p.id
ORDER BY nl.id desc
;
