--  DDL for View V_DI_ACTIVE_PROCESSES
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE VIEW "V_DI_ACTIVE_PROCESSES" ("PROCESS_NAME", "Submission_ID", "COUNT STEPS") AS 
  SELECT 
    p.name "PROCESS_NAME", ss.submission_id "Submission_ID", COUNT(*) "COUNT STEPS"
FROM T_SUBMISSION_STEP ss
JOIN T_SUBMISSION s ON ss.submission_id = s.id
JOIN T_PROCESS_STEP ps ON ss.process_step_id = ps.id
JOIN T_PROCESS p ON s.process_id = p.id
WHERE s.process_id = 88
GROUP BY p.name, ss.submission_id
HAVING COUNT(*) = 1
;
